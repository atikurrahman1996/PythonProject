#Exercise: Write a function that generates a six-digit/character random_user_id

def random_user_id():
    # characters = digits + letters (uppercase + lowercase)
    characters = string.ascii_letters + string.digits
    # pick 6 random characters
    user_id = ''.join(random.choice(characters) for _ in range(6))
    return user_id
print(random_user_id())