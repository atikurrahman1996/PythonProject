
#Built-in Math Functions

x = min(5, 10, 25)
y = max(5, 10, 25)

print(x)
print(y)

#The abs() function returns the absolute (positive) value

x = abs(-7.25)

print(x)

# power function
x = pow(4, 3)
print(x)

#Math Module

import math
x = math.sqrt(64)
print(x)


#The math.pi constant, returns the value of PI (3.14...):

import math
x = math.pi
print(x)